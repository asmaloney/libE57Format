struct E57ValidatorOptions {
    static const int MessageNumberCount = 5000;

    uint64_t    messagesAllowed[MessageNumberCount];

    E57ValidatorOptions() {
        for (int i = 0; i < MessageNumberCount; i++)
            messagesAllowed[i] = 100;
    };
};

