void StructureNodeImpl::setAttachedRecursive()
{
    /// Mark this node as attached to an ImageFile
    isAttached_ = true;

    /// Not a leaf node, so mark all our children
    for (unsigned i = 0; i < children_.size(); i++)
        children_.at(i)->setAttachedRecursive();
}

